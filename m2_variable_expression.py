# -- Numeric Variables and arithmetic --
a = 9
b = 3
c = 10
d = 5

total = b + c
subtract = a - d
product = c * d
quotient = a / b

print(f"The sum of {b} and {c} is {total}.")
print()
print(f"The result of {a} minus {d} is {subtract}.")
print()
print(f"The product of {c} and {d} is {product}.")
print()
print(f"The result of {a} divided by {b} is {quotient}.")
print()

